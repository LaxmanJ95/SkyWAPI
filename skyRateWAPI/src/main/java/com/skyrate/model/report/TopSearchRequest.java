package com.skyrate.model.report;

public class TopSearchRequest {

	String vendorDate;
	String feedbackDate;
	String value;
	int claimedValue;
	
	public String getVendorDate() {
		return vendorDate;
	}
	public void setVendorDate(String vendorDate) {
		this.vendorDate = vendorDate;
	}
	public String getFeedbackDate() {
		return feedbackDate;
	}
	public void setFeedbackDate(String feedbackDate) {
		this.feedbackDate = feedbackDate;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public int getClaimedValue() {
		return claimedValue;
	}
	public void setClaimedValue(int claimedValue) {
		this.claimedValue = claimedValue;
	}
	
	
	
	
}
